// The copyWithin() method copies array elements from one position to another in the given array.

let numbers = ["1", "2", "3", "4"];

numbers.copyWithin(2, 1);

console.log(numbers);

let letters = ["a", "b", "c", "d"];

letters.copyWithin(2, 1);

console.log(letters);

